import java.util.*;
import java.lang.*;
import java.io.*;

public class Codechef_21_Dec_1 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int t = sc.nextInt();
        while (t-- != 0) {
            int x = sc.nextInt();
            int y = sc.nextInt();
            int z = y - x;
            System.out.println(z);
        }
    }
}
